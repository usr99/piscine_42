#include "ft_operator.h"

int	ft_add(int x, int y)
{
	return (x + y);
}

int	ft_substract(int x, int y)
{
	return (x - y);
}

int	ft_multiply(int x, int y)
{
	return (x * y);
}

int	ft_divide(int x, int y)
{
	return (x / y);
}

int	ft_modulo(int x, int y)
{
	return (x % y);
}

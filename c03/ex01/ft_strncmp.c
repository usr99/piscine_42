int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && (unsigned int)i < n)
		i++;
	if ((unsigned int)i == n)
		i--;
	return ((int)(s1[i] - s2[i]));
}

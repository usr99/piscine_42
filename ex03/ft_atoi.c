int	ft_atoi(char *str)
{
	int	nb;
	int	nb_minus;

	nb = 0;
	nb_minus = 0;
	while (*str == ' ')
		str++;
	while (*str == '-' || *str == '+')
	{
		if (*str == '-')
			nb_minus++;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	if (nb_minus % 2 == 1)
		nb *= -1;
	return (nb);
}
